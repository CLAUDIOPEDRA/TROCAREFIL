    private void jButtonConsultarActionPerformed(java.awt.event.ActionEvent evt) { 
        //Criar a vari√°vel de conex√£o
        Connection connection = null;
        PreparedStatement stmt = null;
    try{
        //Procurar a classe no projeto
        Class.forName("com.mysql.jdbc.Driver");
    
        StringBuilder sql = new StringBuilder();
 
       //Cria a conex√£o com o BD
        connection =DriverManager.getConnection("jdbc:mysql//localhost,trocarefil,root,CL4UDI0P3DR4");
        int contador=0;
        sql.append(" SELECT Nome,CPF,Produto FROM clientes ");
        
        if(!jTextField1.getText().trim().isEmpty()){
            sql.append(" WHERE Nome LIKE ? ");
        }
        
        if(!jTextField2.getText().trim().isEmpty()){
            sql.append(" AND CPF = ? ");
        }
        
        stmt = connection.prepareStatement(sql.toString());
        
        if(!jTextField1.getText().trim().isEmpty()){
            stmt.setString(++contador,jTextField1.getText().trim());
        }
        
        if(!jTextField2.getText().trim().isEmpty()){
            stmt.setString(++contador,jTextField2.getText().trim());
        }
        
        ResultSet rs = stmt.executeQuery();
        List<Clientes> listaClientes = new ArrayList<Clientes>();
        Clientes clientes;
        while(rs.next()){
            clientes = new Clientes(rs.getString("Nome"),
                    rs.getString("CPF"),rs.getString("Produto"));
            listaClientes.add(clientes);
        }
        
    }   catch (ClassNotFoundException ex) {
            System.out.println("N√£o foi poss√≠vel encontrar a classe!"+ex.getMessage());
        } catch (SQLException ex) {
            System.out.println("Ocorreu um erro de SQL!"+ex.getMessage());
        }
    finally{
        
        try{
            if(stmt!=null){
                stmt.close();
            }
            if(connection!=null){
               connection.close(); 
            }    
        }catch(SQLException sqlException){
            System.out.println("Ocorreu um erro de SQL!"+sqlException.getMessage());
        }        
    }
}
        
   

